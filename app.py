# 1. 核心數據：將 14:41 截圖數據寫死 (Hardcode)
# 格式：場次, 馬號, 馬名, 舊賠率, 跑法
seeds = [
    (5, 1, "一起美麗", 7.0, "放頭"),
    (5, 4, "做好自己", 3.4, "前領"),
    (5, 10, "知道穩勝", 8.7, "中置"),
    (6, 3, "勇霸龍", 6.4, "前領"),
    (6, 4, "天天友福", 15.0, "前領"),
    (6, 9, "快樂神駒", 5.7, "放頭"),
    (7, 5, "彩虹七色", 4.2, "前領"),
    (7, 6, "星運少爵", 3.0, "前領"),
    (7, 11, "環球英雄", 14.0, "中置")
]

# 2. 聽朝 10:00 AM 手動填入最新賠率 (聽朝改呢度就得)
live_odds = {
    1: 7.0, 4: 3.4, 10: 8.7,  # R5
    3: 6.4, 4: 15.0, 9: 5.7,  # R6
    5: 4.2, 6: 3.0, 11: 14.0  # R7
}

print("\n" + "="*40)
print("      PREDATOR 3T CALIBRATOR")
print("="*40)

for r, no, name, old, pos in seeds:
    now = live_odds.get(no, old)
    bias = ((now - old) / old) * 100
    
    # 標記「落飛」
    tag = " [!] " if bias <= -20 else "     "
    
    # 格式化輸出：場次 | 馬號 | 變動% | 狀態
    print("R%d | #%-2d | %s | %+.1f%% %s" % (r, no, name, bias, tag))

print("-" * 40)
print("策略：$10 單式 3T (每場三隻全入三甲)")
print("="*40 + "\n")
