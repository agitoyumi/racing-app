# -*- coding: utf-8 -*-

def predator_engine():
    # ==========================================
    # 1. 今晚歐聯波膽對標 (03:00 開賽)
    # ==========================================
    football = {
        "賽事": "歐聯 3x1 波膽過關",
        "組合": ["2:2", "3:2", "1:2"],
        "目標": "為聽朝 3T 提供戰備物資"
    }

    # ==========================================
    # 2. 聽日 3T 精確對標 (14:41 原始數據)
    # ==========================================
    # 格式：(馬號, 14:41 賠率, 跑法)
    r5_seeds = [(1, 7.0, "放頭"), (4, 3.4, "前領"), (10, 8.7, "中置")]
    r6_seeds = [(3, 6.4, "前領"), (4, 15.0, "前領"), (9, 5.7, "放頭")]
    r7_seeds = [(5, 4.2, "前領"), (6, 3.0, "前領"), (11, 14.0, "中置")]

    # 聽朝 10:00 AM 手動填入即時賠率 (對標用)
    live_odds = {
        1: 7.0, 4: 3.4, 10: 8.7,   # R5
        3: 6.4, 4: 15.0, 9: 5.7,  # R6
        5: 4.2, 6: 3.0, 11: 14.0  # R7
    }

    # ==========================================
    # 3. 輸出介面 (確保手機睇得到)
    # ==========================================
    print("\n" + "="*40)
    print("      PREDATOR REBORN SYSTEM")
    print("="*40)
    
    # 顯示波膽
    print(f"【今晚波膽】: {' / '.join(football['組合'])}")
    print("-" * 40)
    
    # 顯示馬匹對標
    all_races = [ (5, r5_seeds), (6, r6_seeds), (7, r7_seeds) ]
    
    for r_num, horses in all_races:
        print(f"R{r_num} (3T 目標: 包辦三甲)")
        for no, old, pos in horses:
            now = live_odds.get(no, old)
            bias = ((now - old) / old) * 100
            alert = " [!] 落飛" if bias <= -20 else ""
            print(f"  #{no:<2} {pos:<4} | {old:>4.1f} -> {now:>4.1f} ({bias:>+5.1f}%) {alert}")
        print("-" * 40)

    print("執行指令: 聽朝 10:00 落單 $10 單式 3T")
    print("="*40 + "\n")

if __name__ == "__main__":
    predator_engine()
