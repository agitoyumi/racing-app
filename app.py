import pandas as pd

# ==========================================
# 核心數據區 (基於 4/14 14:41 截圖)
# ==========================================
def get_war_room_data():
    return pd.DataFrame({
        '場次': [5, 5, 5, 6, 6, 6, 7, 7, 7],
        '馬號': [1, 4, 10, 3, 4, 9, 5, 6, 11],
        '馬名': ['一起美麗', '做好自己', ' know心', '勇霸龍', '天天友福', '快樂神駒', '彩虹七色', '星運少爵', '環球英雄'],
        '1441賠率': [7.0, 3.4, 8.7, 6.4, 15.0, 5.7, 4.2, 3.0, 14.0],
        '跑法': ['放頭', '前領', '中置', '前領', '前領', '放頭', '前領', '前領', '中置'],
        '步速預測': ['慢', '慢', '慢', '快', '快', '快', '慢', '慢', '慢']
    })

# ==========================================
# 校對邏輯
# ==========================================
def run_calibration(latest_odds_dict):
    df = get_war_room_data()
    
    # 映射最新賠率
    df['聽朝賠率'] = df['馬號'].map(latest_odds_dict)
    
    # 計算偏差 (Bias) - 搵出「異常資金」
    # 負數代表賠率跌 = 有人落飛
    df['偏差%'] = ((df['聽朝賠率'] - df['1441賠率']) / df['1441賠率'] * 100).round(1)
    
    # 生存篩選：步速 vs 跑法
    # 慢步速對『放頭/前領』加分，快步速對『中置』加分
    def check_logic(row):
        if row['步速預測'] == '慢' and row['跑法'] in ['放頭', '前領']:
            return "✅ 物理優勢 (慢步速+前置)"
        if row['步速預測'] == '快' and row['跑法'] == '中置':
            return "✅ 物理優勢 (快步速+後追)"
        return "⚖️ 正常博弈"

    df['邏輯校對'] = df.apply(check_logic, axis=1)
    return df

# ==========================================
# 聽朝 10:00 執行範例
# ==========================================
# 假設聽朝你填入嘅賠率如下：
morning_odds = {
    1: 6.8, 4: 3.2, 10: 9.0,   # 場 5
    3: 6.0, 4: 14.0, 9: 5.5,   # 場 6
    5: 4.0, 6: 2.9, 11: 15.0   # 場 7
}

final_report = run_calibration(morning_odds)

print("--- 掠食者重生模組：3T 精確對標報告 ---")
print(final_report[['場次', '馬號', '馬名', '偏差%', '邏輯校對']])

# 異常報警
alerts = final_report[final_report['偏差%'] < -25]
if not alerts.empty:
    print("\n⚠️ 警報：發現「大副落飛」異常資金！")
    print(alerts[['場次', '馬號', '馬名', '偏差%']])
